<?php

namespace Model\User\SocialNetwork;


class SocialProfile
{
    protected $userId = "";

    protected $url = "";

    protected $resource = "nekuno";

    function __construct($userId, $url, $resource)
    {
        $this->userId = $userId;
        $this->url = $url;
        $this->resource = $resource;
    }

    /**
     * @return string
     */
    public function getUserId()
    {
        return $this->userId;
    }

    /**
     * @param string $userId
     */
    public function setUserId($userId)
    {
        $this->userId = $userId;
    }

    /**
     * @return string
     */
    public function getUrl()
    {
        return $this->url;
    }

    /**
     * @param string $url
     */
    public function setUrl($url)
    {
        $this->url = $url;
    }

    /**
     * @return string
     */
    public function getResource()
    {
        return $this->resource;
    }

    /**
     * @param string $resource
     */
    public function setResource($resource)
    {
        $this->resource = $resource;
    }



}